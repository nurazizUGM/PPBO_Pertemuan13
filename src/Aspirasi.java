public class Aspirasi {
    private String aspirasi;
    private Rakyat rakyat;

    public Aspirasi(String aspirasi, Rakyat rakyat) {
        this.aspirasi = aspirasi;
        this.rakyat = rakyat;
    }

    public String getAspirasi() {
        return this.aspirasi;
    }

    public Rakyat getRaayat() {
        return this.rakyat;
    }
}
